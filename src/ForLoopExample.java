public class ForLoopExample {
    // Döngüler, bir işlemi tekrar tekrar gerçekleştirmek için kullanılır.

    // For Döngüsü
    public static void main(String[] args){
        for(int i=0;i<5;i++){
            System.out.println("Döngüdeki sayı: "+i);
        }

        for(int i=1; i<10;i++){
            System.out.println(i);
        }
    }
}
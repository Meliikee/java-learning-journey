public class WhileLoopExample {
    // While Döngüsü
    public static void main(String[] args) {
        int i = 0;
        while (i < 5) {
            System.out.println("While döngüsündeki sayı: " + i);
            i++;
        }

        int j = 1;
        while (j < 10) {
            System.out.println(j);
            j++;
        }
    }
}